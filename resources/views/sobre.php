	<p>Modificar o arquivo .env com as informações do seu banco.</p>
	<p>Depois do banco criado digitar o comando para que o laravel crie o migrate.</p>
	<p>php artisan migrate</p>
	<h3>-----  Criando as tabelas  -----</h3>
	<p>php artisan:create_project_table</p>
	<h3>Criar model</h3>
	<p>php artisan make:model project</p>
	<h3 >-----  Criar controller  -----</h3>
	<p>php artisan make:controller projectController</p>
	<h3>-----  Depois de suas views criadas inicialize com o comando  -----</h3>
	<p>php artisan serve</p>
